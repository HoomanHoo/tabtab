package order;

public class AutoOrderSettingDataBean {

	private int mem_code;
	private int medi_code;
	private int trigger_quan;
	private int ao_quan;
	
	public int getMem_code() {
		return mem_code;
	}
	public void setMem_code(int mem_code) {
		this.mem_code = mem_code;
	}
	public int getMedi_code() {
		return medi_code;
	}
	public void setMedi_code(int medi_code) {
		this.medi_code = medi_code;
	}
	public int getTrigger_quan() {
		return trigger_quan;
	}
	public void setTrigger_quan(int trigger_quan) {
		this.trigger_quan = trigger_quan;
	}
	public int getAo_quan() {
		return ao_quan;
	}
	public void setAo_quan(int ao_quan) {
		this.ao_quan = ao_quan;
	}
	
}
