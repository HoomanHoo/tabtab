package order;

public class AutoOrderListDataBean {
	
	private String medi_name;
	private int trigger_quan;
	private int ao_quan;
	private int supply_value;
	
	public String getMedi_name() {
		return medi_name;
	}
	public void setMedi_name(String medi_name) {
		this.medi_name = medi_name;
	}
	public int getTrigger_quan() {
		return trigger_quan;
	}
	public void setTrigger_quan(int trigger_quan) {
		this.trigger_quan = trigger_quan;
	}
	public int getAo_quan() {
		return ao_quan;
	}
	public void setAo_quan(int ao_quan) {
		this.ao_quan = ao_quan;
	}
	public int getSupply_value() {
		return supply_value;
	}
	public void setSupply_value(int supply_value) {
		this.supply_value = supply_value;
	}
	
	
}
