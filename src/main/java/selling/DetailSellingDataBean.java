package selling;

public class DetailSellingDataBean {
	private int mem_code;
	private int s_num;
	private int medi_code;
	private int ds_selling_quan;
	private int ds_selling_price;
	
	public int getMem_code() {
		return mem_code;
	}
	public void setMem_code(int mem_code) {
		this.mem_code = mem_code;
	}
	
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}
	public int getMedi_code() {
		return medi_code;
	}
	public void setMedi_code(int medi_code) {
		this.medi_code = medi_code;
	}
	public int getDs_selling_quan() {
		return ds_selling_quan;
	}
	public void setDs_selling_quan(int ds_selling_quan) {
		this.ds_selling_quan = ds_selling_quan;
	}
	public int getDs_selling_price() {
		return ds_selling_price;
	}
	public void setDs_selling_price(int ds_selling_price) {
		this.ds_selling_price = ds_selling_price;
	}
	
	
}
